    private boolean validar(String nome) {
        String msgErro = "";
        lblNome.setForeground(Color.black);
        lblProd.setForeground(Color.black);

        if (nome.isEmpty()) {
            msgErro = msgErro + "Nome inv√°lido!\n";
            lblNome.setForeground(Color.red);
        }

        if (msgErro.isEmpty()) {
            return true;
        } else {

            JOptionPane.showMessageDialog(this, msgErro, "Erro cadastro PEDIDO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
    
    private void adicionarTabela(Produtos produto) {
        
        ((DefaultTableModel) tblPedido.getModel()).addRow(new Object[3]);

        int linha = tblPedido.getRowCount() - 1;
        int col = 0;
        tblPedido.setValueAt(produto, linha, col++);
        tblPedido.setValueAt(produto.formataPreco(), linha, col++);

    }

